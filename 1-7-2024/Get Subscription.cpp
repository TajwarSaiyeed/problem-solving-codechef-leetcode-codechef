#include <bits/stdc++.h>
using namespace std;

int main()
{
    int tc, x;
    cin >> tc;
    while (tc--)
    {
        cin >> x;
        cout << (x > 30 ? "YES" : "NO") << '\n';
    }
    return 0;
}

