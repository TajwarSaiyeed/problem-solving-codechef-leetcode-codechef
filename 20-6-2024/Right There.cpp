#include <bits/stdc++.h>
using namespace std;

int main()
{
    int tc;
    cin >> tc;
    while (tc--)
    {
        int n, x;
        cin >> n >> x;
        cout << (n <= x ? "YES" : "NO") << "\n";
    }
    return 0;
}

