void solve()
{
    ll n, k;
    cin >> n >> k;

    if (k <= (n + 1) / 2)
    {
        cout << 2 * k - 1 << nl;
    }
    else
    {
        cout << 2 * (k - (n + 1) / 2) << nl;
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int tc = 1;
    // cin >> tc;

    while (tc--)
    {
        solve();
    }

    return 0;
}

